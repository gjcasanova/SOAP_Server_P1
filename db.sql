create table MATCH_LOCATION
(
   LOCATION_ID          int(8) not null auto_increment  comment '',
   CODE                 varchar(3) not null  comment '',
   MATCH_ID             int not null  comment '',
   AVAILABILITY         decimal(3,0) not null  comment '',
   PRICE                decimal(5,2) not null  comment '',
   primary key (LOCATION_ID)
);


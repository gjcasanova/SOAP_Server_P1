create table SOCCER_MATCH
(
   MATCH_ID             int(8) not null auto_increment  comment '',
   LOCAL_TEAM           varchar(64) not null  comment '',
   VISITING_TEAM        varchar(64) not null  comment '',
   DATE                 date not null  comment '',
   SITE                 varchar(128) not null  comment '',
   primary key (MATCH_ID)
);

alter table MATCH_LOCATION add constraint FK_MATCH_LO_REFERENCE_SOCCER_M foreign key (MATCH_ID)
      references SOCCER_MATCH (MATCH_ID) on delete restrict on update restrict;


